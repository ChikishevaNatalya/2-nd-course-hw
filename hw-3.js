// Дано число n=1000. Ваша задача — делить его на 2 до тех пор, 
// пока результат деления не станет меньше 50.

// Определите, какое число получится в результате.Вычислите количество итераций
// (проходов цикла), которые потребовались для достижения результата,
//  и запишите это количество в переменную num.
let n = 1000;
let num = 0;
while (n / 2 >= 50) {
    n /= 2;
    console.log(n)
    num++;
}
console.log(`Итоговое число: ${n}`);
console.log(`Количество делений: ${num}`);

