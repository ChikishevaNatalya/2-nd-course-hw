//4.
//  Есть объект (назовем его obj), который содержит следующие пары ключ-значение:

// "Коля" : '200'
// "Вася" : '300'
// "Петя" : '400'

// Эти пары представляют собой имена сотрудников и их зарплаты в долларах.
//  Ваша задача написать код, который будет проходиться по всем записям этого объекта
//   и выводить информацию о каждом сотруднике в формате: 
// "имя сотрудника" — зарплата "сумма" долларов.

// Для итерации по объекту используйте цикл for-in.


const obj = {
    "Коля": '200',
    "Вася": '300',
    "Петя": '400'
};

for (let i in obj) {
    let salary = obj[i];
    console.log(`${i} - зарплата ${salary} долларов`);
}
