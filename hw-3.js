// В вашей компании пятница является отчетным днем.
// Нужно написать программу, которая: 

// 1. считает дни месяца по датам,
// 2. определяет, какой день пятница,
// 3. выводит сообщение с напоминанием, что нужно подготовить еженедельный отчет.

// Условия задачи:

// Создайте переменную, которая хранит в себе номер первой пятницы месяца (число от 1 до 7).
// Выведите на каждую пятницу месяца (включая полученную) сообщение следующего вида:
//  "Сегодня пятница, ...-е число. Необходимо подготовить отчет."
// Исходите из того, что в нашем месяце 31 день.
//  Должно вывестись от 4 до 5 сообщений с напоминаниями по разным датам.
let firstFriday = 5;


for (let day = firstFriday; day <= 31; day += 7) {
    console.log(`Сегодня пятница, ${day}-е число. Необходимо подготовить отчет.`);
}
// или
let firstFriday = Number(prompt("Введите дату первой пятницы  от 1 до 7"));


for (let day = firstFriday; day <= 31; day += 7) {
    console.log(`Сегодня пятница, ${day}-е число. Необходимо подготовить отчет.`);
}
// или

let dayFriday = 5;
